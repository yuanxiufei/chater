public class IntDemo {

    public static void main(String[] args) {
//         声明类型 byte 类型
        byte bNum = 100;
        System.out.println("输出byte：" + bNum);
//         short 类型
        short sNum = 500;
        System.out.println("输出short：" + bNum);
//         整数类型
        int iNum = 500;
        System.out.println("输出int：" + bNum);
//         长类型
        long lNum = 500;
        System.out.println("输出long：" + bNum);
//         单精度浮点类型
        float fNum = 500;
        System.out.println("输出float类型：" + bNum);
//         双精度类型
        double dNum = 500;
        System.out.println("输出double类型：" + bNum);

    }

}
