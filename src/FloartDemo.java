public class FloartDemo {
    public static void main(String[] args) {

        // 声明dobule类型
        double price = 9.5D;
        System.out.println("汽油的价格：" + price + "元");
        // 生命float类型
        float height = 175.6F;
        System.out.println("身高：" + height + "cm");


        // 计算
        double d1 = 0.1;
        double d2 = 0.2;
        System.out.println(d1 + d2);

    }

}
